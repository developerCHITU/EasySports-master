public class RecordPresenter extends RBasePresenter<RecordContract.IRecordView> implements RecordContract.IRecordPresenter {
    public RecordPresenter(RecordContract.IRecordView view) {
        super(view);
    }

    @Override
    public void getPushUrl() {
        String user_name = MyApp.getCurrentUser().getUser_name();
        addSubscription(MineApiFactory.getPushUrl(user_name).subscribe(new Consumer<PushBean>() {
            @Override
            public void accept(@NonNull PushBean pushBean) throws Exception {
                if (pushBean.getStatus()== C.RESPONSE_SUCCESS) {
                    mView.getPushUrlSuccess(pushBean.getData().getPushUrl());
                }else{
                    mView.getPushUrlFailed(pushBean.getMsg());
                }
            }
        }, new Consumer<Throwable>() {
            @Override
            public void accept(@NonNull Throwable throwable) throws Exception {
                mView.getPushUrlFailed(throwable.getMessage());
            }
        }));
    }
}
