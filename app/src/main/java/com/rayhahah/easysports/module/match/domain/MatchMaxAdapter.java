public class MatchMaxAdapter extends BaseQuickAdapter<MatchStatusBean.MaxPlayers, BaseViewHolder> {
    public MatchMaxAdapter() {
        super(R.layout.item_match_forward_max);
    }

    @Override
    protected void convert(BaseViewHolder helper, MatchStatusBean.MaxPlayers item) {
        helper.setText(R.id.tv_left_player_name, item.leftPlayer.name)
                .setText(R.id.tv_left_player_type, item.leftPlayer.position + "  #" + item.leftPlayer.jerseyNum)
                .setText(R.id.tv_right_player_name, item.rightPlayer.name)
                .setText(R.id.tv_right_player_type, item.rightPlayer.position + "  #" + item.rightPlayer.jerseyNum)
                .setText(R.id.tv_left_value, item.leftVal)
                .setText(R.id.tv_right_value, item.rightVal)
                .setText(R.id.tv_type, item.text);
        GlideUtil.load(mContext, item.leftPlayer.icon, (ImageView) helper.getView(R.id.iv_left_player_icon));
        GlideUtil.load(mContext, item.rightPlayer.icon, (ImageView) helper.getView(R.id.iv_right_player_icon));
    }
}
