public class LiveListAdapter extends BaseQuickAdapter<LiveBean.DataBean, BaseViewHolder> {
    public LiveListAdapter() {
        super(R.layout.item_live_list);
    }

    @Override
    protected void convert(BaseViewHolder helper, LiveBean.DataBean item) {
        helper.setText(R.id.tv_item_screenname, item.getScreenname());
        if (StringUtils.isNotEmpty(item.getPicUrl())) {
            GlideUtil.load(mContext, item.getPicUrl(), (ImageView) helper.getView(R.id.iv_screenshot));
        } else {
            GlideUtil.load(mContext, R.mipmap.ic_logo, (ImageView) helper.getView(R.id.iv_screenshot));
        }
        helper.addOnClickListener(R.id.cl_item_live_list);
    }
}
