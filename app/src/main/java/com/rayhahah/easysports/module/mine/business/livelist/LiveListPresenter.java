public class LiveListPresenter extends RBasePresenter<LiveListContract.ILiveListView> implements LiveListContract.ILiveListPresenter {
    public LiveListPresenter(LiveListContract.ILiveListView view) {
        super(view);
    }

    @Override
    public void getCurrentLive() {
        addSubscription(MineApiFactory.getCurrentLive().subscribe(new Consumer<LiveBean>() {
            @Override
            public void accept(@NonNull LiveBean liveBean) throws Exception {
                if (liveBean.getStatus() == C.RESPONSE_SUCCESS) {
                    mView.getCurrentLiveSuccess(liveBean.getData());
                } else {
                    mView.getCurrentLiveFailed(liveBean.getMsg());
                }
            }
        }, new Consumer<Throwable>() {
            @Override
            public void accept(@NonNull Throwable throwable) throws Exception {
                mView.getCurrentLiveFailed(throwable.getMessage());
            }
        }));
    }
}
