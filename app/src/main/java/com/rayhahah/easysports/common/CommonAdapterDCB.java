public abstract class CommonAdapterDCB<T extends Object> extends DiffUtil.Callback {

    protected List<T> mOldData;
    protected List<T> mNewData;

    public CommonAdapterDCB(List<T> oldData, List<T> newData) {
        mOldData = oldData;
        mNewData = newData;
    }

    @Override
    public int getOldListSize() {
        return mOldData == null ? 0 : mOldData.size();
    }

    @Override
    public int getNewListSize() {
        return mNewData == null ? 0 : mNewData.size();
    }

    @Override
    public boolean areItemsTheSame(int oldItemPosition, int newItemPosition) {
        if (mOldData == null) {
            return false;
        }
        if (mNewData == null) {
            return false;
        }

        return mOldData.get(oldItemPosition).getClass()
                .equals(mNewData.get(newItemPosition).getClass());
    }

    @Override
    public boolean areContentsTheSame(int oldItemPosition, int newItemPosition) {
        T oldBean = mOldData.get(oldItemPosition);
        T newBean = mNewData.get(newItemPosition);
        return areContentsSame(oldBean, newBean);
    }

    public abstract boolean areContentsSame(T oldDataBean, T newDataBean);
}
